        // TODO add your handling code here:
        String Nom=jTextField1.getText();
        String Prenom=jTextField2.getText();
        String Titre=jTextField7.getText();
        String Pays=jTextField3.getText();
        String Institution=jTextField4.getText();
        String Arrivees=jTextField5.getText();
        String Depart=jTextField6.getText();
        
        String req="INSERT INTO conferencier(Nom,Penom,Pays,titre,arrivee,depart,institution) values('"+Nom+"','"+Prenom+"','"+Pays+"','"+Titre+"','"+Arrivees+"','"+Depart+"','"+Institution+"')";
        try{
            stm=conx.connect().createStatement();
            stm.executeQuery(req);
            JOptionPane.showMessageDialog(null, "req execute avec succee!");
        }
        catch(SQLException ex){
            System.err.print(ex);
        }
